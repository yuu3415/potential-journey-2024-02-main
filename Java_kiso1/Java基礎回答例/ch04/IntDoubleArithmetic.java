package ch04;

class IntDoubleArithmetic {

	public static void main(String[] args) {
		// 変数を定義し、値を代入する
		int a = 1234;
		double b = 333.0;

		// 算術演算結果を表示する
		System.out.println(a + " + " + b + " = " + (a + b));
		System.out.println(a + " - " + b + " = " + (a - b));
		System.out.println(a + " * " + b + " = " + (a * b));
		System.out.println(a + " / " + b + " = " + (a / b));
		System.out.println(a + " % " + b + " = " + (a % b));
	}
}
