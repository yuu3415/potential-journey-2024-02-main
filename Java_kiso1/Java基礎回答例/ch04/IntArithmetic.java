package ch04;

class IntArithmetic {

	public static void main(String[] args) {
		// 変数を定義し、値を代入する
		int a = 1234;
		int b = 333;

		// 算術演算結果を表示する
		System.out.println("加算：" + (a + b));
		System.out.println("減算：" + (a - b));
		System.out.println("乗算：" + (a * b));
		System.out.println("除算：" + (a / b));
		System.out.println("剰余算：" + (a % b));
	}
}
